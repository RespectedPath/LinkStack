function fontFaceBlocks(t) {
  const families = [...new Set([t.headingFont, t.bodyFont])];
  const blocks = [];
  for (const fam of families) {
    for (const [file, weight] of FONT_FACES[fam]) {
      blocks.push(
`@font-face {
  font-family: '${fam}';
  font-style: normal;
  font-weight: ${weight};
  font-display: swap;
  src: url("{{ themeAsset('${file}.woff2') }}") format('woff2');
}`);
    }
  }
  return blocks.join('\n');
}

function customHead(t) {
  let bg = '';
  if (t.treatment === 'photo' && t.background) {
    const scrim = t.scrim === 'dark'
      ? 'linear-gradient(180deg, rgba(8,8,10,0.55), rgba(8,8,10,0.78))'
      : 'linear-gradient(180deg, rgba(255,250,242,0.62), rgba(250,243,232,0.84))';
    bg =
`\nbody {
  background-image: ${scrim}, url("{{ themeAsset('${t.background}') }}");
  background-size: cover, cover;
  background-position: center, center;
  background-attachment: fixed, fixed;
  background-repeat: no-repeat, no-repeat;
}`;
  }
  let motif = '';
  if (t.icon) {
    // Faint profession glyph behind the content so the theme reads as its
    // trade. Photo themes already signal, so dial the motif down on them.
    const uri = iconDataUri(t.icon, t.vars['--title-color']);
    const opacity = t.mode === 'dark' ? 0.05 : 0.06;
    motif =
`\nbody::before {
  content: "";
  position: fixed;
  inset: 0;
  z-index: -1;
  pointer-events: none;
  background-image: url("${uri}");
  background-repeat: repeat;
  background-position: center;
  background-size: 78px;
  opacity: ${opacity};
}`;
  }
  return `<style>
${fontFaceBlocks(t)}
${bg}${motif}
</style>
`;
}

