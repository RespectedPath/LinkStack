// Subtle, asset-free background textures (pure CSS) keyed by spec.texture.
function textureCss(t) {
  if (t.treatment !== 'texture') return '';
  const ink = t.mode === 'dark' ? 'rgba(255,255,255,0.035)' : 'rgba(80,55,35,0.05)';
  const patterns = {
    weave: `background-image:
      repeating-linear-gradient(45deg, ${ink} 0 2px, transparent 2px 10px),
      repeating-linear-gradient(-45deg, ${ink} 0 2px, transparent 2px 10px);`,
    dots: `background-image: radial-gradient(${ink} 1.2px, transparent 1.3px);
      background-size: 16px 16px;`,
    grid: `background-image:
      linear-gradient(${ink} 1px, transparent 1px),
      linear-gradient(90deg, ${ink} 1px, transparent 1px);
      background-size: 22px 22px;`,
  };
  return `\n  ${patterns[t.texture || 'dots']}\n  background-attachment: fixed;`;
}

function rootVars(t) {
  return Object.entries(t.vars).map(([k, v]) => `  ${k}: ${v};`).join('\n');
}

